import { Modal, App } from 'obsidian';
import PullComponent from './PullModal.svelte';

export class PullModal extends Modal {
    component: PullComponent;
    constructor(app: App) {
        super(app);
    }

    onOpen() {
        new PullComponent({
            target: this.contentEl,
            props: {
                modal: this,
            },
        });
    }

    onClose() {
        const { contentEl } = this;
        contentEl.empty();
    }
}
